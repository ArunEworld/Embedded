Left Shifting of a 16-bit data
Statement: Program to shift a 16-bit data 1 bit left. Assume data is in the HL register

    HL = 1025 = 0001 0000 0010 0101
     
        HL = 0001 0000 0010 0101
      + HL = 0001 0000 0010 0101
    ----------------------------
    Result = 0010 0000 0100 1010                  